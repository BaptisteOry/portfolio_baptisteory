        <p>En 2009, afin de suivre et comprendre le parcours scolaire d’un français lambda, Baptiste retourne à <a href="https://fr.wikipedia.org/wiki/Vitr%C3%A9_(Ille-et-Vilaine)" title="Vitré">Vitré</a> pour étudier au collège Sainte Jeanne d’Arc, puis au lycée Sainte Jeanne d’Arc. Soucieux du sort des montreuillais, mais devant tout de même partager son savoir dans le monde, Baptiste choisit de faire pendant sept ans, soir et matin, le trajet en <a href="https://fr.wikipedia.org/wiki/Transport_scolaire" title="Transport scolaire">car scolaire</a> entre sa maison familiale et son école.</p>
        <p>La même année, Baptiste commence une impressionnante collection de <a href="https://fr.wikipedia.org/wiki/Super_Picsou_g%C3%A9ant" title="Super Picsou Géant">Super Picsou Géant</a>. Elle compte à ce jour plus de 200 numéros.</p>
        <h3 id="carriere_sportive">Carrière sportive
            <span class="editsection">
                <span>[</span>
                <a title="Modifier la section : Carrière sportive">modifier</a>
