        </h3>
        <div class="thumbnail_r">
            <a target="_blank" href="images/maison_montreuil.jpg"><img title="Maison familiale de Baptiste à Montreuil sous Pérouse" src="images/maison_montreuil.jpg"></a>
            <div class="legend_2">Maison familiale de Baptiste à <a href="https://fr.wikipedia.org/wiki/Montreuil-sous-P%C3%A9rouse" title="Montreuil sous Pérouse">Montreuil sous Pérouse</a></div>
        </div>
        <p>En 2006, les parents de Baptiste déménagent à <a href="https://fr.wikipedia.org/wiki/Montreuil-sous-P%C3%A9rouse" title="Montreuil sous Pérouse">Montreuil Sous Pérouse</a> : un petit village situé 4 kilomètres au nord de <a href="https://fr.wikipedia.org/wiki/Vitr%C3%A9_(Ille-et-Vilaine)" title="Vitré">Vitré</a>. Il les suit encore une fois, malgré sa tristesse d’abandonner ses camarades vitréens tout chamboulés. À Montreuil et à l’école primaire le Sacré-Cœur, Baptiste découvre avec émois les difficultés auxquelles font face les petites gens de la <a href="https://fr.wikipedia.org/wiki/Campagne" title="Campagne">campagne</a>. Il considérera néanmoins ces années champêtres comme les plus belles de sa vie et aimera se rappeler la beauté et l'insouciance de cette vie simple.</p>
        <p>Malheureusement, le petit village de <a href="https://fr.wikipedia.org/wiki/Montreuil-sous-P%C3%A9rouse" title="Montreuil sous Pérouse">Montreuil-sous-Pérouse</a> gèrera très mal le passage de Baptiste Ory : quelques années plus tard, <a href="https://fr.wikipedia.org/wiki/Pillage" title="Pillage">pilleurs</a> et <a href="https://fr.wikipedia.org/wiki/Tourisme_de_masse" title="Tourisme de masse">tourisme de masse</a> déformeront à tout jamais la charmante vallée encaissée et son ravissant bocage<a href="#cite_note-2" class="reference">2</a>.</p>

