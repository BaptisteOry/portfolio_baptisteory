            <div class="legend_2">Baptiste apprenant aux <a href="https://fr.wikipedia.org/wiki/Qu%C3%A9b%C3%A9cois" title="Québecois">Québecois</a> à faire du <a href="https://fr.wikipedia.org/wiki/Patin_%C3%A0_glace" title="Patin à glace">patin à glace</a></div>
