        </h3>
        <p>Au lycée, Baptiste impressionne ses camarades et professeurs de par ses réponses aux devoirs toujours très justes et très complètes, mais aussi et surtout de par ses exposés novateurs. En 2015, il joue devant la classe de latin une scène du <a href="https://fr.wikipedia.org/wiki/Hippolyte_(Euripide)" title="Phèdre (Euripide)">Phèdre d’Euripide</a> avec 600 figurants en toge blanche.</p>
        <p>L’année de son <a href="https://fr.wikipedia.org/wiki/Baccalaur%C3%A9at_scientifique" title="Baccalauréat scientifique">baccalauréat scientifique</a>, Baptiste aide tant et si bien ses amis et collègues lycéens que Sainte Jeanne d’Arc est classé premier lycée d’Ille et Vilaine<a href="#cite_note-6" class="reference">6</a>. Enfant de la cinquième république, l’émoi de la mention « très bien » sur son diplôme aux reflets dorés<a href="#cite_note-7" class="reference">7</a> le motive alors à poursuivre ses études dans le système public d’<a href="https://fr.wikipedia.org/wiki/%C3%89tudes_sup%C3%A9rieures_en_France" title="Études supérieures en France">enseignement supérieur français</a>.</p>

