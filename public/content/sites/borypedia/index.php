        </h3>
        <p>À l’âge de 12 ans, Baptiste se met avec vigueur à la <a href="https://fr.wikipedia.org/wiki/Natation" title="Natation">natation</a>. Malgré un réel talent pour la <a href="https://fr.wikipedia.org/wiki/Brasse_(natation)" title="Natation">brasse</a>, il n’apprécie pas l’ambiance de compétition malsaine qui règne dans les piscines et le chlore qui agresse sa belle peau lustrée. Il ne participera qu’à une seule compétition : le désormais fameux 2e circuit départemental d’<a href="https://fr.wikipedia.org/wiki/Ille-et-Vilaine" title="Ille-et-Vilaine">Ille-et-Vilaine</a>, à <a href="https://fr.wikipedia.org/wiki/Chartres-de-Bretagne" title="Chartres-de-Bretagne">Chartres-de-Bretagne</a>. Le 6 février 2010, des spectateurs chanceux ont ainsi pu voir Baptiste Ory faire un aller-retour en brasse de 50 mètres en à peine 15 secondes, avant de s'arrêter à 30 centimètres de l’arrivée pour laisser les neuf autres nageurs prépubères passer devant lui<a href="#cite_note-3" class="reference">3</a>.</p>
        <h3 id="oeuvre_artistique">Œuvre artistique
            <span class="editsection">
                <span>[</span>
                <a title="Modifier la section : Œuvre artistique">modifier</a>
                <span> | </span>
                <a title="Modifier la section : Œuvre artistique">modifier le code</a>
