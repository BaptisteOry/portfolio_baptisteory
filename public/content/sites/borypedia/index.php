        </h3>
        <div class="thumbnail_l">
            <a target="_blank" href="images/oeuvre_baptistorisme.jpg"><img alt="Œuvre baptistoriste : « Promène » (2019)" src="images/oeuvre_baptistorisme.jpg"></a>
            <div class="legend_2">Œuvre baptistoriste : « Promène » (2019)</div>
        </div>
        <p>À la suite de cet événement inédit, Baptiste se retire des bassins. L’émotion est générale dans toute la région. Il choisit alors de consacrer ses activités parascolaires à l’art. <a href="https://fr.wikipedia.org/wiki/Th%C3%A9%C3%A2tre" title="Théâtre">Théâtre</a>, <a href="https://fr.wikipedia.org/wiki/Peinture" title="Peinture">peinture</a> et <a href="https://fr.wikipedia.org/wiki/Dessin" title="Dessin">dessin</a> rythmeront ses mercredis après-midi, et vendredis soir<a href="#cite_note-4" class="reference">4</a><sup class="reference cite_virgule">,</sup><a href="#cite_note-5" class="reference">5</a>. L’école d’<a href="https://fr.wikipedia.org/wiki/Arts_plastiques" title="Arts plastiques">arts plastiques</a> et le conservatoire d’<a href="https://fr.wikipedia.org/wiki/Th%C3%A9%C3%A2tre" title="Art dramatique">art dramatique</a> de <a href="https://fr.wikipedia.org/wiki/Vitr%C3%A9_(Ille-et-Vilaine)" title="Vitré">Vitré</a> voient alors naître en leur sein un nouveau courant artistique : le Baptistorisme. Ce mouvement consiste en un humour acerbe et une palette colorée. Baptiste Ory en est le chef de file et le seul représentant. Le Baptistorisme sera condamné de nombreuses fois par le <a href="https://fr.wikipedia.org/wiki/Vatican" title="Vatican">Vatican</a> qui le considérait comme un « art décadent ».</p>
        <h3 id="genie_compris">Génie compris
            <span class="editsection">
                <span>[</span>
                <a title="Modifier la section : Génie compris">modifier</a>
