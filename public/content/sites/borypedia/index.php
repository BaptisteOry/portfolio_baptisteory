        <p>En 2018, Baptiste part en <a href="https://fr.wikipedia.org/wiki/Nouvelle-France" title="Nouvelle-France">Nouvelle-France</a> afin de parfaire ses connaissances sur le Nouveau Monde et valider son diplôme. À son arrivée, le continent vierge d’une telle énergie voit sa météo s’emballer et une vague de froid paralyse la région pendant plus d’une semaine<a href="#cite_note-8" class="reference">8</a>.</p>
        <p>Baptiste réalise en groupe, au <a href="https://fr.wikipedia.org/wiki/C%C3%A9gep_de_Matane" title="Cégep de Matane">Cégep de Matane</a>, un projet multimédia novateur : « Balloune ». Ce jeu de plateforme contrôlable à la main permet à des milliers d’enfants de s’amuser tout en se remuant. Plusieurs enfants <a href="https://fr.wikipedia.org/wiki/Super_Picsou_g%C3%A9ant" title="Paraplégie">paraplégiques</a> se sont même levés et remis à marcher pour jouer au jeu<a href="#cite_note-9" class="reference">9</a>.</p> 
        <p>Après un stage en webdesign à <a href="https://fr.wikipedia.org/wiki/Montr%C3%A9al" title="Paris">Montréal</a> où il relança la croissance de toute la ville<a href="#cite_note-10" class="reference">10</a>, Baptiste décide de revenir dans son pays natal. À son arrivée en <a href="https://fr.wikipedia.org/wiki/France" title="France">France</a> le 19 juin 2018, des hourras, banderoles de bienvenues et pleurs égaient le hall d’arrivées de l’<a href="https://fr.wikipedia.org/wiki/A%C3%A9roport_de_Paris-Charles-de-Gaulle" title="Aéroport de Paris-Charles-de-Gaulle">aéroport Paris-Roissy Charles de Gaulle</a>. Il fut un temps envisagé de le renommer « aéroport Baptiste Ory », mais il a été déterminé qu'il y avait un risque de confusion avec celui d'<a href="https://fr.wikipedia.org/wiki/A%C3%A9roport_de_Paris-Orly" title="Aéroport de Paris-Orly">Orly</a>.</p>
