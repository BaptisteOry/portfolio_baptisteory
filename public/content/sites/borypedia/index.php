        </h3>
        <p>À <a href="https://fr.wikipedia.org/wiki/Vitr%C3%A9_(Ille-et-Vilaine)" title="Vitré">Vitré</a>, Baptiste étudie à l’école maternelle Notre-Dame, puis à l’école primaire Sainte Jeanne d’Arc. Il se mélange aux jeunes enfants d’origines diverses (<a href="https://fr.wikipedia.org/wiki/Vitr%C3%A9_(Ille-et-Vilaine)" title="Vitré">Vitré</a>, <a href="https://fr.wikipedia.org/wiki/Balaz%C3%A9" title="Balazé">Balazé</a>, <a href="https://fr.wikipedia.org/wiki/Champeaux_(Ille-et-Vilaine)" title="Champeaux">Champeaux</a>, <a href="https://fr.wikipedia.org/wiki/Val-d%27Iz%C3%A9" title="Val d’Izé">Val d’Izé</a>, <a href="https://fr.wikipedia.org/wiki/Erbr%C3%A9e" title="Erbrée">Erbrée</a>, etc.). Il restera fortement marqué par cette expérience scolaire multiculturelle. Ses camarades de classe seront aussi fortement bousculés et transformés par sa venue. À l’heure actuelle, la moitié d’entre eux fait partie des <a href="https://fr.wikipedia.org/wiki/Time_100" title="Time 100">personnes les plus influentes du début du <abbr class="abbr" title="21ᵉ siècle"><span class="romain">XXI</span><sup style="font-size:72%">e</sup></abbr> siècle</a> (l’autre moitié à fondée une secte consacrée à sa personne et est morte lors d’un <a href="https://fr.wikipedia.org/wiki/Suicide_collectif" title="Suicide collectif">suicide collectif</a>).</p>
        <h3 id="annees_champetres">Années champêtres
            <span class="editsection">
                <span>[</span>
                <a title="Modifier la section : Années champêtres">modifier</a>
