        <div class="thumbnail_r">
            <a target="_blank" href="images/baptiste_mort.jpg"><img alt="Vue d'artiste de la mort de Baptiste Ory" src="images/baptiste_mort.jpg"></a>
            <div class="legend_2">Vue d'artiste de la mort de Baptiste Ory</div>
