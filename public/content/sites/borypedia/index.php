        <p>De plus en plus passionné par le <a href="https://fr.wikipedia.org/wiki/Multim%C3%A9dia" title="Multimédia">multimédia</a>, domaine qu’il considère comme la dernière parcelle du monde à explorer et défricher (avant l’espace), Baptiste s’inscrit sur « <a href="https://fr.wikipedia.org/wiki/Admission_Post-Bac" title="Admission Post-Bac">Admission Post-Bac</a> » dans de ce type de formations. Il est cependant accepté dans toutes les formations de tous les domaines d’Europe. Il choisit d’étudier à l’<a href="https://fr.wikipedia.org/wiki/Institut_universitaire_de_technologie_de_Laval" title="IUT de Laval">Institut universitaire de technologie de Laval</a> les <a href="https://fr.wikipedia.org/wiki/Dipl%C3%B4me_universitaire_de_technologie_en_m%C3%A9tiers_du_multim%C3%A9dia_et_de_l%27Internet" title="Diplôme universitaire de technologie en métiers du multimédia et de l'Internet">métiers du multimédia et de l’Internet</a>. Toujours modeste, il opte pour habiter dans un appartement d’à peine 20 mètres carrés. Une frontière le sépare alors chaque semaine de ses racines, du <a href="https://fr.wikipedia.org/wiki/Beurre_demi-sel" title="Beurre demi-sel">beurre demi-sel</a> et de la <a href="https://fr.wikipedia.org/wiki/Galette-saucisse" title="Galette-saucisse">galette-saucisse</a>. Il sortira deux ans plus tard major de sa promotion (bien sûr).</p>
