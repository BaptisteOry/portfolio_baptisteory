        <p>Baptiste choisit de poursuivre son cursus à l’école d’ingénieur IMAC - la seule école d’ingénieur publique alliant arts et sciences - afin de réaliser le rêve de son enfance et le combat de sa vie d’adulte : allier enfin les arts et les sciences. Il s’installe à <a href="https://fr.wikipedia.org/wiki/Bussy-Saint-Georges" title="Bussy-Saint-Georges">Bussy-Saint-Georges</a>, une ville nouvelle de l’est de <a href="https://fr.wikipedia.org/wiki/Paris" title="Paris">Paris</a>, et choisit - toujours plus modeste - un appartement encore plus petit que le précédent. <a href="https://fr.wikipedia.org/wiki/Disneyland_Paris" title="Disneyland Paris">Disneyland Paris</a>, à quelques kilomètres de là, perd donc un nombre considérable de visiteurs : ceux-ci préfèrent aller à Bussy afin de tenter d’apercevoir Baptiste à se fenêtre et déposer des offrandes en bas de sa résidence.</p>
        <p>Ses projets et œuvres novateurs ne cessent d'impressionner et révolutionner le monde de l'image, du multimédia, de l'audiovisuel et de la communication. C'est au même moment qu'il devient le fan numéro 1 du groupe de musique <a href="https://fr.wikipedia.org/wiki/The_Pirouettes" title="The Pirouettes">The Pirouettes</a>. Il montre ainsi au monde qu'il n'est pas qu'un faiseur : il peut lui aussi être admirateur de ses semblables.</p>

