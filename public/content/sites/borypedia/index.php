        <p>Baptiste Ory vit à <a href="https://fr.wikipedia.org/wiki/Fontainebleau" title="Fontainebleau">Fontainebleau</a> jusqu’à ses deux ans. Les gens de la région se souviennent de lui comme un très beau bébé, souriant, intelligent et déjà très attentif aux problèmes du monde. Par la suite, il décide de suivre ses parents lorsqu’ils retournent en <a href="https://fr.wikipedia.org/wiki/Bretagne" title="Bretagne">Bretagne</a> : là où sont ses racines. Il passe donc le reste de son enfance, puis son adolescence, dans le <a href="https://fr.wikipedia.org/wiki/Canton_de_Vitr%C3%A9" title="Canton de Vitré">canton de Vitré</a> (<a href="https://fr.wikipedia.org/wiki/Ille-et-Vilaine" title="Ille-et-Vilaine">Ille-et-Vilaine</a>).</p>
        <h3 id="retour_aux_origines">Retour aux origines
            <span class="editsection">
                <span>[</span>
                <a title="Modifier la section : Retour aux origines">modifier</a>
