        <p><b>Baptiste Ory</b>, surnommé « <b>Baptiste Ory</b> », né le <a href="https://fr.wikipedia.org/wiki/1er_ao%C3%BBt" title="1er août">1<sup style="font-size:83.33%; line-height:1">er</sup></a> <a href="https://fr.wikipedia.org/wiki/Ao%C3%BBt_1998" title="Août 1998">août</a> <a href="https://fr.wikipedia.org/wiki/1998_en_France" title="1998 en France">1998</a> à <a href="https://fr.wikipedia.org/wiki/Fontainebleau" title="Fontainebleau">Fontainebleau</a>, est un <a href="https://fr.wikipedia.org/wiki/Intellectuel" title="Intellectuel">intellectuel</a>, <a href="https://fr.wikipedia.org/wiki/Artiste" title="Artiste">artiste</a>, <a href="https://fr.wikipedia.org/wiki/Influenceur" title="Influenceur">influenceur</a>, <a href="https://fr.wikipedia.org/wiki/Humoriste" title="Humoriste">humoriste</a>, <a href="https://fr.wikipedia.org/wiki/Chanteur" title="Chanteur">chanteur</a>, <a href="https://fr.wikipedia.org/wiki/Acteur" title="Acteur">acteur</a>, <a href="https://fr.wikipedia.org/wiki/R%C3%A9alisateur" title="Réalisateur">réalisateur, <a href="https://fr.wikipedia.org/wiki/Philanthropie" title="Philantrope">philanthrope</a> et <a href="https://fr.wikipedia.org/wiki/Ing%C3%A9nieur" title="Ingénieur">ingénieur créatif</a> français. Il est surtout connu pour être Baptiste Ory.</p>

