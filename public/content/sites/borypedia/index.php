        <p>Le <?php echo strftime("%d %B %Y", $dateDeath); ?>, à 20h26, Baptiste Ory est retrouvé mort sur le chemin entre son appartement et son école. Un plat de <a href="https://fr.wikipedia.org/wiki/Spaghetti" title="Spaghetti">pâtes spaghettis</a> - son repas préféré, car il le disait sain et aux conséquences écologiques limitées - s’écoule de sa bouche aux lèvres gercées. Il lui manque son bras droit et son pied gauche. Il tient dans sa main restante une <a href="https://fr.wikipedia.org/wiki/Marguerite_commune" title="Marguerite commune">marguerite</a>.</p>
        <p>Les réactions à sa mort sont sans égales : <a href="https://fr.wikipedia.org/wiki/Deuil_national" title="Deuil national">deuil national</a> d’un mois, création d’un <a href="https://fr.wikipedia.org/wiki/Jour_f%C3%A9ri%C3%A9" title="Jour ferié">jour férié</a>, milliards de personnes dans les rues, <a href="https://fr.wikipedia.org/wiki/Inondation" title="Inondation">inondations</a> provoquées par les pleurs et <a href="https://fr.wikipedia.org/wiki/S%C3%A9isme" title="Tremblement de terre">tremblements de terre</a> de <a href="https://fr.wikipedia.org/wiki/Magnitude_(sismologie)" title="Magnitude (sismologie)">magnitude</a> 12 en plusieurs endroits dans le monde. Malgré de multiples hypothèses et des enquêtes d’envergure mondiale, les raisons de la mort de Baptiste Ory sont à ce jour toujours inconnues.</p>
        <p>Il reste de son passage sur Terre une gigantesque production dans tous les domaines qu’il affectionnait et d'innombrables personnes changées à jamais par sa rencontre.</p>

