        </h2>
        <div class="thumbnail_l">
            <a target="_blank" href="images/fontainebleau_rayon.jpg"><img alt="Rayon lumineux sur l'hôpital de Fontainebleau" src="images/fontainebleau_rayon.jpg"></a>
            <div class="legend_2">Rayon lumineux sur l'hôpital de <a href="https://fr.wikipedia.org/wiki/Fontainebleau" title="Fontainebleau">Fontainebleau</a> le 1<sup style="font-size:83.33%;line-height:1">er</sup> août 1998</div>
        </div>
        <p>Baptiste Erwan Pierrick naît le <a href="https://fr.wikipedia.org/wiki/1er_ao%C3%BBt" title="1er août">1<sup style="font-size:83.33%; line-height:1">er</sup></a> <a href="https://fr.wikipedia.org/wiki/Ao%C3%BBt_1998" title="Août 1998">août</a> <a href="https://fr.wikipedia.org/wiki/1998_en_France" title="1998 en France">1998</a> à <a href="https://fr.wikipedia.org/wiki/Fontainebleau" title="Fontainebleau">Fontainebleau</a> (<a href="https://fr.wikipedia.org/wiki/Seine-et-Marne" title="Seine-et-Marne">Seine-et-Marne</a>). Au même moment, un gigantesque rayon lumineux blanc éclaire le <a href="https://fr.wikipedia.org/wiki/Maternit%C3%A9_(%C3%A9tablissement)" title="Maternité (établissement)">service maternité</a> de l’hôpital. Ce dernier sera aperçu jusqu'à une distance de 250 kilomètres<a href="#cite_note-1" class="reference">1</a>. Dans les rues de <a href="https://fr.wikipedia.org/wiki/Fontainebleau" title="Fontainebleau">Fontainebleau</a>, la foule en extase s’amasse et commence à chanter des <a href="https://fr.wikipedia.org/wiki/Cantique" title="Cantique">cantiques</a> à propos d’un certain « sauveur ». La population est dispersée dans l’après-midi avec des lances à eau et l'événement sera dissimulé pendant de nombreuses années par le gouvernement.</p>

